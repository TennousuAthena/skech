#include <stdio.h>

int main(void)
{
    double profit; // 利润
    double bonus; // 提成奖金
    printf("Enter the profit: ");
    scanf("%lf", &profit); // 从键盘输入利润

    if (profit <= 100000) // 利润低于或等于10万元的
    {
        bonus = profit * 0.1;
    }
    else if (profit <= 200000) // 利润高于10万元，低于20万元时
    {
        bonus = 100000 * 0.1 + (profit - 100000) * 0.075;
    }
    else if (profit <= 400000) // 利润高于20万低于40万元的
    {
        bonus = 100000 * 0.1 + 100000 * 0.075 + (profit - 200000) * 0.05;
    }
    else // 利润高于40万元的
    {
        bonus = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + (profit - 400000) * 0.03;
    }

    printf("The bonus is: %.2f\n", bonus); // 输出应发的提成

    return 0;
}
