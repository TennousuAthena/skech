#include <stdio.h>

// 定义函数dele用于删除字符串p中的数字字符
void dele(char *s, char *p) {
  int i = 0, j = 0;

  // 遍历字符串p
  while (p[i] != '\0') {
    // 如果当前字符不是数字字符，则保存到s字符串中
    if (p[i] < '0' || p[i] > '9') {
      s[j] = p[i];
      j++;
    }
    i++;
  }
  s[j] = '\0';  // 添加字符串结尾
}

int main() {
  char s[30] = "abc12ef5ghij8yz";  // 初始化字符串s
  char p[30] = "";  // 初始化字符串p

  printf("原字符串s：%s\n", s);  // 输出原字符串s

  // 调用函数dele删除s字符串中的数字字符
  dele(p, s);

  printf("处理后的新字符串p：%s\n", p);  // 输出处理后的新字符串p

  return 0;
}
