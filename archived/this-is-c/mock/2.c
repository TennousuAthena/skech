/**
 * 已知一个整型数组A[8]={ 744,9799,282,692,90,6106,2,99}
 * 请输出其中个位与十位相同的数字及下标
 * 要求在主函数中定义数组，
 * 通过数组初始化方式赋值，
 * 并在主函数中输出原数组及如下结果：
  
  A[0]=744 A[1]=9799 A[7]=99
  
  有 3 个满足条件
  

 */
#include<stdio.h>

int main(){
	int A[8]={ 744,9799,282,692,90,6106,2,99};
	
	for(int i=0; i<8; i++){
		if(A[i] % 10 == (A[i]/10)%10)
			printf("A[%d]=%d ", i, A[i]);
	}
}
