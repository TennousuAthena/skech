#include <stdio.h>

int main()
{
	/* Declaring and initializing the variables */
	float result1 = 3.0 * 5.0;
	float result2 = 7.1 * 8.3 - 2.2;
	float result3 = 3.2 / (6.1 * 5);
	
	/* Displaying the results of the arithmetic expressions */
	printf("Result of expression 3.0*5.0 is: %f\n", result1);
	printf("Result of expression 7.1*8.3-2.2 is: %f\n", result2);
	printf("Result of expression 3.2/(6.1*5) is: %f\n", result3);
	
	return 0;
}
